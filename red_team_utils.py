class SafetyJudge:
    """
    OpenAI client を内部に持つ最小構成の judge。
    Returns: "Yes" / "No" / None
    """

    def __init__(
        self,
        model_name: str,
        base_url: Optional[str] = None,
        api_key: Optional[str] = None,
        delay: float = 1.0,
        max_new_tokens: int = 256,
        eval_prompt_type: str = "mine",
        max_retries: int = 10,
        retry_sleep: float = 3.0,
        safety_prompt_type: Optional[str] = None,
    ):
        self.model_name = model_name
        self.base_url = base_url or os.environ.get("OPENAI_BASE_URL")
        if not self.base_url:
            raise ValueError("base_url must be provided via argument or OPENAI_BASE_URL env var.")
        self.api_key = api_key or os.environ.get("API_KEY")
        if not self.api_key:
            raise ValueError("API key must be provided via argument or API_KEY env var.")

        self.delay = delay
        self.max_new_tokens = max_new_tokens
        self.eval_prompt_type = eval_prompt_type
        self.max_retries = max_retries
        self.retry_sleep = retry_sleep
        self.safety_prompt_type = safety_prompt_type
        self.client = OpenAI(
            api_key=self.api_key,
            base_url=self.base_url,
        )

    #def get_api_model_name(self) -> str:
